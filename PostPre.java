class PostPre{

     public static void main(String []args){
         int i=5;
         int j;
         j=i++;
         System.out.println(j+" "+i);
         int k=5;
         k=k++;
         System.out.println(k);
         int r=++k;
         System.out.println(r);
         int l=4;
         l=--l;
         System.out.println(l);
         
     }
}