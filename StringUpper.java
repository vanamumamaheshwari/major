import java.io.*;
import java.util.*;
class StringUpper{
public static void main(String a[])
{
String s1,s2;
Scanner sc=new Scanner(System.in);
s1=sc.nextLine();
s2=sc.nextLine();
s1=s1.toUpperCase();
s2=s2.toUpperCase();
System.out.println("Upper of String1 :"+s1);
System.out.println("Upper of String2 :"+s2);
}
}

