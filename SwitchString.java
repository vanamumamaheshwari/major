import java.util.Scanner;

import java.io.*;

class SwitchString{

     
public static void main(String []args){
               
Scanner sc=new Scanner(System.in);
       
String i=sc.nextLine();
         
switch(i){
             
case "Hi":
                 
System.out.print("Hi");
                 
break;
                  
case "Hello":
                 
System.out.print("Hello");
                 
break;
                 
default:
                   
System.out.print("Hey");
                 
         
}
      
     
}

}